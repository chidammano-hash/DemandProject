-- 172: Drop unused indexes flagged by pg_stat_user_indexes (idx_scan = 0).
--
-- Background:
--   The DB performance audit identified two indexes consuming significant
--   storage (~775 MB combined) with zero scans recorded since the last stats
--   reset. Dropping them frees disk and removes write amplification on every
--   INSERT/UPDATE to the parent tables.
--
-- ----------------------------------------------------------------------------
-- 1. fact_sales_monthly_sales_ck_key (519 MB, idx_scan=0)
-- ----------------------------------------------------------------------------
-- Auto-generated by the UNIQUE constraint on `sales_ck`. Sales rows are
-- already uniquely identified by the primary key, and no ETL upsert paths
-- target `sales_ck` directly (sales loads use INSERT ... ON CONFLICT (sk)
-- semantics or full-table replace). Verified safe to drop.
--
-- The index is auto-created by a UNIQUE constraint, so a plain DROP INDEX is
-- rejected by Postgres ("cannot drop index because constraint requires it").
-- Drop the constraint instead — Postgres removes the backing index atomically.
-- ALTER TABLE briefly takes AccessExclusive; for a hot table consider running
-- during a maintenance window.

ALTER TABLE fact_sales_monthly DROP CONSTRAINT IF EXISTS fact_sales_monthly_sales_ck_key;

-- ----------------------------------------------------------------------------
-- 2. uq_forecast_ck_model (267 MB, idx_scan=0)  -- KEEP, DO NOT DROP
-- ----------------------------------------------------------------------------
-- REVIEW: this index backs the UNIQUE constraint (forecast_ck, model_id) on
-- fact_external_forecast_monthly. While pg_stat_user_indexes reports zero
-- scans, the constraint IS used at write time by the ETL upsert paths:
--
--   * scripts/etl/load_ext_ml_forecasts.py:181
--       INSERT ... ON CONFLICT (forecast_ck, model_id) DO UPDATE SET ...
--   * scripts/etl/load_backtest_forecasts.py:512
--       INSERT ... ON CONFLICT (forecast_ck, model_id) DO UPDATE SET ...
--   * scripts/ml/run_champion_selection.py:628
--       INSERT ... ON CONFLICT (forecast_ck, model_id) DO NOTHING
--
-- ON CONFLICT requires an enforcing unique index; dropping uq_forecast_ck_model
-- would break every external-forecast and backtest load. The "0 scans" reading
-- is misleading because conflict-checks during INSERT do not increment idx_scan
-- the same way SELECT lookups do.
--
-- Decision: leave the index in place. If future audits confirm it is truly
-- redundant (e.g. another unique index on the same columns is added), revisit
-- in a separate migration.
--
-- DROP INDEX CONCURRENTLY IF EXISTS uq_forecast_ck_model;  -- intentionally commented out
